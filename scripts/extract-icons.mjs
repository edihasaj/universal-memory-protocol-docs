// One-off dev tool: extract the icons we use from the eunifyer icon set and
// vendor them into src/components/icons.generated.ts (committed, so CI needs no
// access to the eunifyer repo). Re-run locally if the icon set changes.
//
//   node scripts/extract-icons.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

